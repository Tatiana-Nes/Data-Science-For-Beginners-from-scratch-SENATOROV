"""Виртуальное окружение."""

# 1.  Что делает команда python -m venv venv?
#     Создает виртуальное окружение в папке venv
#
#     1.1 Что делает каждая команда в списке ниже?
#         pip list - Показывает установленные в текущем окружении пакеты и их версии в виде таблицы.
#         pip freeze > requirements.txt - выгружает зависимости в файл requirements.txt
#         pip install -r requirements.txt - устанавливает зависимости из файл в файл requirements.txt
#
# 2.  Что делает каждая команда в списке ниже?
#     conda env list - показывает все среды Conda и путь к ним
#     conda create -n env_name python=3.5 - создаёт новое виртуальное окружение с именем env_name и установленным Python 3.5.
#     conda env update -n env_name -f file.yml - Обновляет виртуальное окружение env_name (Добавляет/обновляет пакеты) по спецификации из YAML.
#     source activate env_name - активирует виртуальное окружение env_name
#     source deactivate - деактивирует текущее виртуальное окружение
#     conda clean -a - чистит кеш
#
