# 11. Зачем нужно виртуально окружение?
#     Для того, чтобы при работе над разными проектами, где нам необходимы разные пакеты, избежать конфликтов библиотек
#     Все пакеты лежат внутри отдельной папки, в случае чего можно легко удалить окружение, не трогая систему.
