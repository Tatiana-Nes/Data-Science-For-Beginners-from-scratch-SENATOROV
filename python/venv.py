# 4.  Как установить необходимые пакеты внутрь виртуального окружения для conda/venv?
#     Чтоьбы установить пакеты внутрь окружения через conda нужно выполнить команду:
#     conda install pandas numpy
#
#     Чтоьбы установить пакеты внутрь venv:
#     python -m pip install --upgrade pip
#     pip install pandas numpy
#
# 5.  Что делают эти команды?
#     pip freeze > requirements.txt - выгружает зависимости в файл requirements.txt
#     conda env export > environment.yml - экспортирует текущее окружение в файл environment.yml
#
