# 6.  Что делают эти команды?
#     pip install -r requirements.txt - устанавливает зависимости из файла requirements.txt
#     conda env create -f environment.yml - считывает окружение из файла environment.yml
#
# 7.  Что делают эти команды?
#     pip list - показывает установленные в текущем окружении пакеты и их версии в виде таблицы.
#     pip show - показывает подробную информацию об одном пакете: версию, путь установки, зависимости
#     conda list - показывает все пакеты в активном окружении conda, включая установленные через pip
#
# 8.  Где по умолчанию больше пакетов venv/pip или conda? и почему дата сайнинисты используют conda?
#     В conda. Именно по этой причине дата сайнинисты используют conda, она предоставляет большинство необходимых пакетов для работы в Дата сайнс
#     Не смотря на то, что pip больше пакетов (через PyPI), дата-сайентисты используют conda, так как она обеспечивает простую установку тяжёлых научных библиотек, (numpy, pandas, pytorch и т.д.)
